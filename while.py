treeHit = 0                                     #나무 찍은 횟수 0으로 초기화
while treeHit < 10:                             #10회 이하인 동안 반복
    treeHit = treeHit +1                        #횟수+1
    print("나무를 %d번 찍었습니다." % treeHit)   #횟수 출력
    if treeHit == 10:                           #10번 찍으면
         print("나무 넘어갑니다.")               #나무 넘어감      
         exit                                   #탈출 
         
         
         
        